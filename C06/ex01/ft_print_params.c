#include <unistd.h>

int	main(int ac, char **ag)
{
	int idx;

	idx = 1;
	while (idx < ac)
	{
		while (*ag[idx])
			write(1, ag[idx]++, 1);
		write(1, "\n", 1);
		++idx;
	}
	return (0);
}
