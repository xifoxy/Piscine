int ft_is_prime(int nb)
{
    long long idx;

    if (nb < 2)
        return (0);
    if (nb < 4)
        return (1);
    idx = 2;
    while (idx * idx <= nb)
        if (nb % idx == 0)
            return (0);
    return (1);
}